// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2)
  {
    return num1;
  }else if (num2 < num1)
  {
    return num2;
  }
  return num1;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num % 2 === 0)
    return true;
  else
    return false;
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
    var array = string.split("");
    var count = 0;
    for (var i = 0; i < array.length; i++)
    {
      if (array[i] === char)
      {
        ++count;
      }
    }
    return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var array = string.split("");
  var count = 0;
  for (var i = 0; i < array.length; i++)
  {
    if (array[i] === 'B')
    {
      ++count;
    }
  }
  return count;
}

